    private void OloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OloginActionPerformed
        // TODO add your handling code here: 
        //login
        if(username.getText().trim().isEmpty() && password.getText().trim().isEmpty()){
        lbl_username.setText("Username is Empty");
        lbl_password.setText("Password is Empty");
        }else if(username.getText().trim().isEmpty()){
            lbl_username.setText("Username is Empty");
        }else if(password.getText().trim().isEmpty()){
            lbl_password.setText("Password is Empty");
        }else{try {
            String sql = "Select * from operator where username=? and password=?";
            PreparedStatement pst= con.prepareStatement(sql);
            pst.setString(1,username.getText());
            pst.setString(2,password.getText());
            ResultSet rs=pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null,"Welcome back to the Operator page.");
                operator_main_page Y1 =new operator_main_page();
                Y1.setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null,"Username or Password Incorrect.");
                username.setText("");
                password.setText("");
                username.requestFocus();  
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null,e);
        }}
    }//GEN-LAST:event_OloginActionPerformed

